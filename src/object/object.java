package object;

import java.awt.*;

public class object{
    public int x, y, width, height;
    public Image img;
    public object (int x, int y, int width, int height, Image img) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.img = img;
    }

}
